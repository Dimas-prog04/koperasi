<script setup>
import { onMounted } from 'vue'

onMounted(() => {
    /* ============================================
       AKSELERASI BISNIS LANDING PAGE - VANILLA JS
       ============================================ */
    
    // Semua manipulasi DOM aman di sini karena PASTI dieksekusi di sisi client/browser!

    // ==========================================
    // NAVBAR SCROLL EFFECT
    // ==========================================
    const navbar = document.querySelector('.navbar');
    let lastScroll = 0;

    window.addEventListener('scroll', () => {
        const currentScroll = window.pageYOffset;

        if (navbar) {
            if (currentScroll > 50) {
                navbar.style.boxShadow = '0 4px 20px rgba(27, 67, 50, 0.08)';
            } else {
                navbar.style.boxShadow = 'none';
            }
        }
        lastScroll = currentScroll;
    });

    // ==========================================
    // FEATURES CAROUSEL
    // ==========================================
    const featureCards = document.querySelectorAll('.feature-card');
    const featureDots = document.querySelectorAll('.features .dot');
    const arrowPrev = document.querySelector('.arrow-prev');
    const arrowNext = document.querySelector('.arrow-next');
    let currentFeature = 1; // Center card is active by default

    function updateFeatureCarousel(index) {
        featureCards.forEach((card, i) => {
            card.style.transition = 'all 0.5s cubic-bezier(0.4, 0, 0.2, 1)';

            if (i === index) {
                card.style.transform = 'translateY(-10px) scale(1)';
                card.style.opacity = '1';
                card.style.zIndex = '3';
                card.style.boxShadow = '0 12px 40px rgba(27, 67, 50, 0.15)';

                const icon = card.querySelector('.card-icon');
                if (icon) {
                    icon.classList.remove('gray');
                    icon.classList.add('green');
                }
            } else if (i < index) {
                card.style.transform = 'rotate(-5deg) translateY(10px) scale(0.95)';
                card.style.opacity = '0.85';
                card.style.zIndex = '1';
                card.style.boxShadow = '0 4px 24px rgba(27, 67, 50, 0.08)';

                const icon = card.querySelector('.card-icon');
                if (icon) {
                    icon.classList.remove('green');
                    icon.classList.add('gray');
                }
            } else {
                card.style.transform = 'rotate(5deg) translateY(10px) scale(0.95)';
                card.style.opacity = '0.85';
                card.style.zIndex = '1';
                card.style.boxShadow = '0 4px 24px rgba(27, 67, 50, 0.08)';

                const icon = card.querySelector('.card-icon');
                if (icon) {
                    icon.classList.remove('green');
                    icon.classList.add('gray');
                }
            }
        });

        featureDots.forEach((dot, i) => {
            dot.classList.toggle('active', i === index);
        });
    }

    if (arrowPrev) {
        arrowPrev.addEventListener('click', () => {
            currentFeature = (currentFeature - 1 + featureCards.length) % featureCards.length;
            updateFeatureCarousel(currentFeature);
        });
    }

    if (arrowNext) {
        arrowNext.addEventListener('click', () => {
            currentFeature = (currentFeature + 1) % featureCards.length;
            updateFeatureCarousel(currentFeature);
        });
    }

    featureDots.forEach((dot, index) => {
        dot.addEventListener('click', () => {
            currentFeature = index;
            updateFeatureCarousel(currentFeature);
        });
    });

    // ==========================================
    // TESTIMONIALS SLIDER
    // ==========================================
    const testimonialData = [
        {
            name: 'Siti Aminah',
            role: 'Ibu Rumah Tangga',
            avatar: 'https://i.pravatar.cc/150?img=5',
            quote: '"Menghemat biaya sewa web developer puluhan juta. Sangat disarankan untuk UMKM yang baru merintis!"',
            stars: 5
        },
        {
            name: 'Budi Santoso',
            role: 'Pemilik Warung Kopi',
            avatar: 'https://i.pravatar.cc/150?img=11',
            quote: '"Website saya jadi lebih profesional dan pelanggan meningkat 3x lipat dalam sebulan. Luar biasa!"',
            stars: 5
        },
        {
            name: 'Dewi Lestari',
            role: 'Pengusaha Batik',
            avatar: 'https://i.pravatar.cc/150?img=9',
            quote: '"Tidak perlu coding sama sekali, AI langsung buatkan website yang cantik dan responsif."',
            stars: 5
        }
    ];

    const testimonialCard = document.querySelector('.testimonial-card');
    const navPrev = document.querySelector('.nav-prev');
    const navNext = document.querySelector('.nav-next');
    const navDots = document.querySelectorAll('.nav-dot');
    let currentTestimonial = 0;

    function updateTestimonial(index) {
        const data = testimonialData[index];
        if (!testimonialCard) return;

        testimonialCard.style.opacity = '0';
        testimonialCard.style.transform = 'translateX(-20px)';

        setTimeout(() => {
            const img = testimonialCard.querySelector('.testimonial-avatar img');
            const h4 = testimonialCard.querySelector('.testimonial-info h4');
            const span = testimonialCard.querySelector('.testimonial-info span');
            const quote = testimonialCard.querySelector('.testimonial-quote');
            
            if (img) { img.src = data.avatar; img.alt = data.name; }
            if (h4) h4.textContent = data.name;
            if (span) span.textContent = data.role;
            if (quote) quote.textContent = data.quote;

            const starsContainer = testimonialCard.querySelector('.testimonial-stars');
            if (starsContainer) {
                starsContainer.innerHTML = '';
                for (let i = 0; i < data.stars; i++) {
                    starsContainer.innerHTML += '<i class="fas fa-star"></i>';
                }
            }

            testimonialCard.style.opacity = '1';
            testimonialCard.style.transform = 'translateX(0)';
        }, 300);

        navDots.forEach((dot, i) => {
            dot.classList.toggle('active', i === index);
        });
    }

    if (navPrev) {
        navPrev.addEventListener('click', () => {
            currentTestimonial = (currentTestimonial - 1 + testimonialData.length) % testimonialData.length;
            updateTestimonial(currentTestimonial);
        });
    }

    if (navNext) {
        navNext.addEventListener('click', () => {
            currentTestimonial = (currentTestimonial + 1) % testimonialData.length;
            updateTestimonial(currentTestimonial);
        });
    }

    navDots.forEach((dot, index) => {
        dot.addEventListener('click', () => {
            currentTestimonial = index;
            updateTestimonial(currentTestimonial);
        });
    });

    setInterval(() => {
        currentTestimonial = (currentTestimonial + 1) % testimonialData.length;
        updateTestimonial(currentTestimonial);
    }, 6000);

    // ==========================================
    // CTA GENERATE BUTTON
    // ==========================================
    const generateBtn = document.querySelector('.btn-generate');
    const ctaInput = document.querySelector('.cta-input-group input');

    if (generateBtn && ctaInput) {
        generateBtn.addEventListener('click', () => {
            const value = ctaInput.value.trim();
            if (value) {
                generateBtn.innerHTML = '<i class="fas fa-spinner fa-spin"></i> Generating...';

                setTimeout(() => {
                    generateBtn.innerHTML = '<i class="fas fa-check"></i> Generated!';
                    generateBtn.style.background = '#2d6a4f';
                    generateBtn.style.color = '#fff';

                    setTimeout(() => {
                        generateBtn.innerHTML = '<i class="fas fa-wand-magic-sparkles"></i> Generate';
                        generateBtn.style.background = '';
                        generateBtn.style.color = '';
                    }, 2000);
                }, 2000);
            } else {
                ctaInput.style.borderColor = '#ef4444';
                ctaInput.placeholder = 'Please describe your idea first...';

                setTimeout(() => {
                    ctaInput.style.borderColor = '';
                    ctaInput.placeholder = 'Buatkan saya web dimsum mental...';
                }, 2000);
            }
        });

        ctaInput.addEventListener('keypress', (e) => {
            if (e.key === 'Enter') {
                generateBtn.click();
            }
        });
    }

    // ==========================================
    // SMOOTH SCROLL FOR NAV LINKS
    // ==========================================
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });

    // ==========================================
    // INTERSECTION OBSERVER FOR ANIMATIONS
    // ==========================================
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, observerOptions);

    document.querySelectorAll('.feature-card, .testimonial-card, .stat-item, .cta-container').forEach(el => {
        el.style.opacity = '0';
        el.style.transform = 'translateY(20px)';
        el.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
        observer.observe(el);
    });

    // ==========================================
    // MAP PINS ANIMATION
    // ==========================================
    const mapPins = document.querySelectorAll('.map-pin');
    mapPins.forEach((pin, index) => {
        pin.style.animation = `bounce 2s ease-in-out ${index * 0.3}s infinite`;
    });

    const styleSheet = document.createElement('style');
    styleSheet.textContent = `
        @keyframes bounce {
            0%, 100% { transform: translateY(0); }
            50% { transform: translateY(-8px); }
        }
    `;
    document.head.appendChild(styleSheet);
});
</script>

